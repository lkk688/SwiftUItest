struct CaptureImageView {
    /// MARK: - Properties
    @Binding var isShown: Bool
    @Binding var image: Image?
    @Binding var text: String?
    
    func makeCoordinator() -> Coordinator {
      return Coordinator(isShown: $isShown, image: $image, text: $text)
    }
}

